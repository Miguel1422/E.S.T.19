    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        Config.setIP(tfIP.getText());
        String pass="";
        char p [] = tfPass.getPassword();
        for (int i = 0; i < p.length; i++) {
            pass+=p[i];
        }
        Config.setPASS(pass);
        Config.setUSER(tfUser.getText());
        this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Conexion c = new Conexion();
        ImageIcon icono = new ImageIcon(getClass().getResource("/image/palomita.png"));
        Config.setIP(tfIP.getText());
        String pass="";
        char p [] = tfPass.getPassword();
        for (int i = 0; i < p.length; i++) {
            pass+=p[i];
        }
        Config.setPASS(pass);
        Config.setUSER(tfUser.getText());
        
        
        
        /*System.out.println("TF: " + tfUser.getText());
        System.out.println("Config: " + Config.getUSER());
        System.out.println("Conexion: " + Conexion.getUSER());*/
        if (c.probarConexion()) {
           //System.out.println( "Correcto");
            JOptionPane.showMessageDialog(this, "Correcto", "", JOptionPane.INFORMATION_MESSAGE, icono);
            btnAceptar.setEnabled(true);
            tfIP.setEnabled(false);
            tfUser.setEnabled(false);
            tfPass.setEnabled(false);
        } else {
            //System.out.println("No se pudo conectar");
            JOptionPane.showMessageDialog(this, "Error informacion incorrecta", "", JOptionPane.ERROR_MESSAGE, null);
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

